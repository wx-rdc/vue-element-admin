      </p>
      <p>
        那晚，男人离开后，阿莲剥开了糖衣，往口里送了一颗糖，廉价的酥糖一股直白浓烈的糖精味，但不知怎地，她的泪水就是不受控制的滑落，打湿了一片草席。
